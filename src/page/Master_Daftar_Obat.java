    private void id_obat() {
        try {
            koneksi connect = new koneksi(); // memanggil class koneksi
            connect.konek();
            String sql = "SELECT * FROM daftar_obat ORDER BY id_daftar_obat DESC";
            Statement stat = connect.stm;
            ResultSet res = stat.executeQuery(sql);

            if (res.next()) {
                String id_obat = res.getString("id_daftar_obat").substring(2);
                String AN = "" + (Integer.parseInt(id_obat) + 1);
                String Nol = "";

                if (AN.length() == 1) {
                    Nol = "000";
                } else if (AN.length() == 2) {
                    Nol = "00";
                } else if (AN.length() == 3) {
                    Nol = "0";
                } else if (AN.length() == 4) {
                    Nol = "";
                }

                txt_id_obat.setText("HO" + Nol + AN);

            } else {
                txt_id_obat.setText("HO0001");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

