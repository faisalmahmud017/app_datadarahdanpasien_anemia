    public void DB_Data_tabel() { // getData() dibuat untuk memanggil data pasien dari database dan menampilkannya di tabel_pasien

        try {

// memanggil data pasien dari database yaitu di tabel pasien yg ada di databas
            koneksi connect = new koneksi(); // memanggil class koneksi
            connect.konek();
//            Connection con = connect.con;
            Statement stat = connect.stm;
            String sql = "SELECT * FROM daftar_obat ORDER BY id_daftar_obat DESC";
            ResultSet res = stat.executeQuery(sql);

//pengecekan terhadap data pasien di database
// get.String disesuaikan dengan field data pasien yg di databse yaitu di tabel pasien
            while (res.next()) {
                Object[] obj = new Object[13];
                obj[0] = res.getString("id_daftar_obat");
                obj[1] = res.getString("tipe_obat");
                obj[2] = res.getString("nama_obat");
                obj[3] = res.getString("harga_obat");

                tabel_model.addRow(obj);
            }

        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }

